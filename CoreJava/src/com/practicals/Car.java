package com.practicals;

public class Car {
    private int door;
    private int wheels;
    private String model;

    public void setDoor(int door) {
        this.door = door;
    }

    public void setWheels(int wheels) {
        this.wheels = wheels;
    }

    public void setModel(String model) {
        this.model = model;
    }

    //Getters
    public int getDoor() {
        return door;
    }

    public int getWheels() {
        return wheels;
    }

    public String getModel() {
        return model;
    }


}
